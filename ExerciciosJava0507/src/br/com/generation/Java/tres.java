package revisaoBasica;

public class tres {

	public static void main(String[] args) {

		int soma1, soma2, somamedia, mediamedia;
		
		soma1 = (8+9+7)/3;
		soma2 = (4+5+6)/3;
		somamedia = soma1+soma2;
		mediamedia = (soma1+soma2)/2;
		
		System.out.println("A média da primeira soma é: " + soma1);
		System.out.println("A média da segunda soma é: " + soma2);
		System.out.println("A soma das duas média é: " + somamedia);
		System.out.println("A média das duas médias é: " + mediamedia);
	}

}
